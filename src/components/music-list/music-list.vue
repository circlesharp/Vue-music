<template>
  <div class="music-list" ref='musicList'>
    <div class="back" @click='back'>
      <i class="icon-back"></i>
    </div>
    <h1 class="title" v-html='title'></h1>
    <div class="bg-image" :style='bgStyle' ref='bgImage'>
      <div class="play-wrapper" v-show='songs.length' ref='playWrapper'>
        <div class="play">
          <i class="icon-play"></i>
          <span class="text" @click='randomPlayAll'>随机播放全部</span>
        </div>
      </div>
      <div class="filter" ref='filter'></div>
    </div>
    <div class="bg-layer" ref="layer"></div>
    <Scroll class="list" @scroll='scroll' :data='songs' :probeType='probeType' :listenScroll='listenScroll' ref='list'>
      <div class="song-list-wrapper">
        <!-- SongList组件的元素被点击后会emit select 事件 -->
        <SongList :songs='songs' @select='selectItem' :rank='rank'></SongList>
      </div>
      <div class="loading-container" v-show='!songs.length'>
        <Loading></Loading>
      </div>
    </Scroll>
  </div>
</template>

<script>
import Scroll from 'base/scroll/scroll'
import SongList from 'base/song-list/song-list'
import Loading from 'base/loading/loading'
import { prefixStyle } from 'common/js/dom'
import { mapActions, mapMutations } from 'vuex'
import { playlistMixin } from 'common/js/mixin'

const RESERVED_HEIGHT = 40
const transform = prefixStyle('transform')
const backdrop = prefixStyle('backdrop-filter')

export default {
  mixins: [playlistMixin],
  props: {
    bgImage: {
      type: String,
      default: ''
    },
    songs: {
      type: Array,
      default () { return [] }
    },
    title: {
      type: String,
      default: ''
    },
    rank: {
      type: Boolean,
      default: false
    }
  },
  data () {
    return {
      scrollY: 0
    }
  },
  computed: {
    bgStyle () {
      return `background-image: url(${this.bgImage})`
    }
  },
  created () {
    this.probeType = 3
    this.listenScroll = true
  },
  mounted () {
    this.imageHeight = this.$refs.bgImage.clientHeight
    this.minTranslateY = this.imageHeight - RESERVED_HEIGHT
    this.$refs.list.$el.style.top = `${this.imageHeight}px`
  },
  watch: {
    scrollY (newY) {
      let translateY = Math.min(this.minTranslateY, newY)
      let zIndex = 0
      let scale = 1
      let blur = 0

      this.$refs.layer.style[transform] = `translate3d(0, -${translateY}px, 0)`

      const percent = Math.abs(newY / this.imageHeight)
      if (newY < 0) {
        scale = 1 + percent
        zIndex = 10
      } else {
        blur = Math.min(20 * percent, percent)
      }
      this.$refs.filter.style[backdrop] = `blur(${blur}px)`

      if (newY > this.minTranslateY) {
        zIndex = 10
        this.$refs.bgImage.style.paddingTop = 0
        this.$refs.bgImage.style.height = `${RESERVED_HEIGHT}px`
        this.$refs.playWrapper.style.display = 'none'
      } else {
        this.$refs.bgImage.style.paddingTop = '70%'
        this.$refs.bgImage.style.height = 0
        this.$refs.playWrapper.style.display = 'block'
      }
      this.$refs.bgImage.style.zIndex = zIndex
      this.$refs.bgImage.style[transform] = `scale(${scale})`
    }
  },
  methods: {
    handlePlaylist (playlist) {
      const bottom = playlist.length > 0 ? '60px' : ''
      this.$refs.musicList.style.bottom = bottom
      this.$refs.list.refresh()
    },
    scroll (pos) {
      this.scrollY = -pos.y
    },
    back () {
      this.$router.back()
    },
    selectItem (item, index) {
      this.selecePlay({
        list: this.songs,
        index
      })
    },
    randomPlayAll () {
      this.randomPlay({
        list: this.songs
      })
    },
    ...mapActions([
      'selecePlay', 'randomPlay'
    ]),
    ...mapMutations({
      setFullScreen: 'SET_FULL_SCREEN'
    })
  },
  components: {
    Scroll, SongList, Loading
  }
}
</script>

